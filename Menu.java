	static String choixMenu()
	{
		//Click
		
		while(1!=0)
		{
			Point pos=new Point();
			pos=Isn.readClick();

			//Click JOUER
			if(200<=pos.x && pos.x<=599)
			{
				if(250<=pos.y && pos.y<=349)
					{
						return "jeu";
					}
			}

			//Click REGLES
			if(200<=pos.x && pos.x<=599)
			{
				if (450<=pos.y && pos.y<=549)
					{
						Isn.paintRect(0,0,800,800,20,20,20);
						return "regles";
					}
			}		
		}
	}

	static void affichageRegles()
	{
		Isn.paintRect(0,0,800,750,0,0,0);
		
		//affichage
		int [][][] image;
		int i, j;
		image=Isn.getImageData("ecrans/apropos.ppm");
		
		for(j=0;j<image[0].length;j=j+1)
		{
			for(i=0;i<image.length;i=i+1)
			{
				Isn.drawPixel(i,j,image[i][j][0],image[i][j][1],image[i][j][2]);
			}
		}
	}


